package Main;

public class CubeNode {
	private int count, side, stick, dir, numb1, numb2;

	public int getSide() {
		return side;
	}

	public void setSide(int side) {
		this.side = side;
	}

	public int getStick() {
		return stick;
	}

	public void setStick(int stick) {
		this.stick = stick;
	}

	public int getDir() {
		return dir;
	}

	public void setDir(int dir) {
		this.dir = dir;
	}

	public int getNumb1() {
		return numb1;
	}

	public void setNumb1(int numb1) {
		this.numb1 = numb1;
	}

	public int getNumb2() {
		return numb2;
	}

	public void setNumb2(int numb2) {
		this.numb2 = numb2;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
